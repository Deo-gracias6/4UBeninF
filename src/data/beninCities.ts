// Get unique regions
export const regions: ('Nord' | 'Centre' | 'Sud')[] = ['Nord', 'Centre', 'Sud'];

// Helper function to get city by ID
export const getCityById = (id: string): BeninCity | undefined => {
  return beninCities.find(city => city.id === id);
};

// Helper function to get cities by department
export const getCitiesByDepartment = (department: string): BeninCity[] => {
  return beninCities.filter(city => city.department === department);
};

// Helper function to get cities by region
export const getCitiesByRegion = (region: 'Nord' | 'Centre' | 'Sud'): BeninCity[] => {
  return beninCities.filter(city => city.region === region);
};
